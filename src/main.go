//在军队成功控制天安门广场后，中国大陆当局开始针对参与示威抗议的群众展开大规模逮捕行动，并且也撤除相关工作或者支持抗议群众的政府官员的职务[297]。根据北京市委办公厅所编出版的《1989北京制止动乱，平息反革命暴乱纪事》提到，北京市在“六四”后共抓捕了1,103名涉及“暴乱”的疑犯。有市民因为仅仅在街上拿走士兵遗下的军用包就被控以抢劫罪名，判处七年、十年的监禁。[298]。许多参与六四事件的人士之后陆续流亡海外，包括柴玲、吾尔开希等学生领袖便透过香港组织的黄雀行动，逃往美国、英国、法国以及其他西方国家[299][300][301]，之后则被中国大陆当局长期禁止返回中国大陆境内[302]。不过陈子明和王军涛则是在1989年年底准备流亡时遭到逮捕，中国大陆当局指控他们为整起抗议活动的“幕后黑手”并且于1990年判处13年有期徒刑[303][304]。
//
//中国大陆有关当局陆续逮捕或者拘留了共计数万名来自中国大陆各地的群众，而根据对话基金会援引各省级人民政府的资料指出在1989年春天时有1,602名与抗议活动有关的人士被判处有期徒刑[305]。许多人被安排至监狱监禁或者是送往劳改营，并且被禁止与他们的家人会面。而相关单位除了时常对这些涉嫌参与示威活动的群众以酷刑施压外，还将这些持有不同意见者与杀人犯或者强奸犯安排在同一个牢房内；同时由于许多人遭到逮捕使得牢房空间不足，甚至没有足够的空间让每个人都可以入睡[306]。上海监狱和劳改队开展“平息反革命暴乱”为内容的思想政治教育，播放《飘扬，共和国的旗帜》等录像[307]。截至2012年5月为止，仍然至少有2名参与抗议活动人士仍然关押在北京市，另外还有5人则下落不明[305]。另外曾经担任赵紫阳助手的鲍彤则被指控泄露国家机密和反革命宣传，并于1992年7月被判有期徒刑7年[308]。
//
//王丹和赵长青则是少数几位被列为通缉犯并遭到逮捕的学生领袖，被列为学生领袖通缉名单之首的王丹先是被判处4年的有期徒刑[309]，在1998年以保外就医的理由，离开监狱并获准移民前往美国居住[310]；他在哈佛大学获得学士学位后，主要从事学术的工作[311]。赵常青因为被视为在抗议活动中影响力较小的公众人物，在为期6个月的监禁后获得释放，但之后由于继续要求中共展开政治改革而再度遭关押。吾尔开希在事件后逃往台湾，并长期在中央广播电台从事政治评论员的工作[312]；之后他曾三度表态愿意自首[313]，并希望返回中国大陆探视亲人，不过最后都被遣返回台湾[314]。李录则在华尔街成为投资银行家，并且成立了一家公司[311]。柴玲先是流亡法国，之后再转到美国寻求政治庇护；后来她于美国高科技公司工作，并成立了关注中国大陆女性权益和计划生育问题的非营利组织女童之声（All Girls Allowed）[311][315]。
//
//2008年11月3日至11月21日期间，禁止酷刑委员会举办第四十一届会议，并根据《联合国禁止酷刑公约》第19条审议会员国提交的报告。当中禁止酷刑委员会对于中国大陆当局处理六四事件的方式感到担忧，认为尽管许多家属提出“在1989年6月4日于北京镇压行动中遭杀害、逮捕或失踪”的申诉，但中国大陆当局却迟迟没有展开调查[316]。同时禁止酷刑委员会也提到中国大陆当局并未告诉家属相关人士的下落，而对过度动用武力的负责人也无任何行政或者刑事处分[316]。不过在2009年12月，中国大陆当局回应禁止酷刑委员会建议时表示已经就“1989年春夏之交的政治风波”完全结案[317]，并强调当时采取及时且果断的手段是必要和正确的[317]。同时中国大陆当局认为将六四事件形容成“民主运动”已经扭曲了事件想要推翻共产党专政的本质[317]，并且认为这些意见与禁止酷刑委员会的职责并不符合[317]。
