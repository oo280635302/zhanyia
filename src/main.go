//各国、各地区反应
//参见：对于六四事件的反应
//事件发生后，国际社会对中国政府的做法评价不一。国际社会大多数谴责中华人民共和国政府以武力驱离示威群众，许多西方国家亦一度实施经济制裁，并至今实行武器禁运[104]。对于中国政府通过武力镇压处理抗议活动的方式引起来自西欧、东欧、北美洲、澳大利亚和一些西亚与拉丁美洲国家的批评[358]，其中西方国家政府和新闻媒体更是对此严厉谴责[359]。但是值得注意的是许多亚洲国家则在抗议期间持续保持沉默，其中印度政府一方面下令国家电视台减少相关报导以免损害与中国之间的关系，但是另一方面也对于因为中国军队清场而死亡的民众表达同情之意[360]。而多数对该事件作出反应的非洲国家则认为该事件属于中国内政，并且对该事件表示同情。[361]根据日本外务省在2020年12月23日公开的1989年前后外交文件[362]，日本政府在6月4日当天决定对中国和睦，并反对西方对中国制裁[363]。而包括朝鲜、巴基斯坦、古巴、罗马尼亚、捷克斯洛伐克、东德和一些中东及非洲国家则表态支持中国政府的作法，并且谴责相关的抗议活动进行[359]。许多海外的中国留学生为了表示支持天安门广场上的示威活动，于欧洲、美洲、中东和亚洲等地的城市发起示威活动，并且开始在互联网上架构提供新闻服务的《华夏文摘》和创建非政府组织中国民主后援国际；而在中国军队以武力驱散天安门广场上的示威群众后，这些海外华人则组织如中国民主团结联盟和全美中国学生学者自治联合会等团体，并且在1990年代中期以前拥有一定的政治影响[364]。
//
//然而对于许多西方国家来说，中国政府对于天安门广场上的抗议活动的处理方式破坏了自身的国际声誉。许多西方国家新闻媒体从原本计划采访5月举行的中苏首脑会议而转向报导有关早期戒严行动的情况，示威群众抓住这个机会并且开始为国际电视台的观众制作标志和旗帜，而这也使得西方社会对于中国学生的抗议活动多表示同情。而在中国政府对于如何处理媒体报导示威活动而犹豫不决时，也意味着为中国和海外记者报导抗议活动时提供了一个相对宽松的环境。抗议活动的影像纪录在事件之后20年强烈影响了西方对于中国的看法和相关政策[365]。其中阻挡坦克行进的王维林图像变成该事件重要的标志，被称为“坦克人”，且长期在西方社会成为公民反抗专制政权的象征[366]。《时代》杂志更是以“无名的反抗者”将其列入20世纪100位最重要的人物之一[367]。
//
//另外一方面，对于当时仍然是英国殖民地的香港来说，六四事件的发生使得社会大众恐惧在1997年主权移交后，中国是否会违背一国两制的承诺[368]。对此香港总督彭定康试图针对立法局展开政治改革，但这让香港政府和中国政府间发生摩擦。而对于许多香港民众来说，天安门广场上的抗议遭到镇压成为了一个转捩点，许多民众因此对中国政府失去了信任[369]；再加上当时香港市民普遍对香港主权移交后的地位感到不确定，使得1997年前出现了大规模的移民潮，许多人前往加拿大或澳大利亚等西方国家生活。英国政府为了阻止人才随着移民潮而外流，同意让香港政府施行居英权计划，授予部分香港民众正式的英国公民身份[370]。而自1990年后，香港每年都有数万名民众参与由香港市民支援爱国民主运动联合会主办的大型“维园六四烛光晚会”活动。香港在1997年主权转移给中国后，相关的纪念活动仍在持续进行[371]。
