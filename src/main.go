//4月30日，中共中央总书记赵紫阳从朝鲜平壤返国并重新掌握党政权力。然而随着外界要求中国政府对学生示威活动的态度更为软化后，内部相关的讨论冲突反而更为加剧。以赵紫阳为首的温和派，主张继续与学生展开对话；以国务院总理李鹏为首的强硬派，则主张应该强硬地反对抗议活动继续进行。在5月1日召开的中共中央政治局常务委员会议上，赵紫阳和李鹏再度针对这一议题有所冲突。当中赵紫阳认为先前强硬派的作法已经证明并无实际的效力，因此政府特别允许这次活动才是唯一的选择[146]。
