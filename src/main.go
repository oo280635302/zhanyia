//对于李鹏认为国家的稳定发展应该优先于任何事项，赵紫阳则反驳说中国共产党应该表态支持扩大民主和提升透明度的要求。最后在赵紫阳强力推行下，政府决定展开进一步的对话[147]。赵紫阳随后开放新闻媒体积极报导抗议活动的发展，并在5月3日至5月4日期间发表了两次同情示威群众的演讲[148]。赵紫阳发言中提到学生关切政府官员贪腐的问题是正当的，同时认为这次学生运动应该被视为一种爱国表现[149][147]。在5月4日当天，有十万名学生在北京街头游行以纪念五四运动[150]，同时再度重申先前示威游行所提出的要求[151]。赵紫阳的发言实际基本上否定了4月26日《人民日报》发表的社论内容[152]，这让很多大学生都满意政府所做的让步[153]。5月4日结束时，除了北京大学和北京师范大学外，所有北京市的大学皆宣布罢课行动结束，随后大部分学生也逐渐失去参与抗议活动的兴趣，新闻自由获得口头保障，多数人主张以对话渐进推动民主[153]。
