//死伤人数
//
//6月6日后的一天，北京街头一辆烧毁的车辆
//由于中国官方拒绝提供更多事件资料，导致数据模糊且出现多种版本，单是死亡人数的估计便从百余人至上万人都有[318]。1989年6月6日，中华人民共和国国务院官员召开新闻发表会，发言人袁木表示“初步统计”包括部队士兵、大学学生、非法分子和误杀群众在内，有近300人死亡[319][320]。袁木还提到有5,000名中国人民解放军的士官和士兵受伤，而包括违法暴徒和围观群众在内、有2,000名平民受伤[319][321]。根据北京市警方的调查，在北京市遭到杀害的平民“包括大学教授、技术人员、政府官员、工厂工人、小型私营企业拥有人、退休工人、高中学生和小学学生等，其中最年轻的仅有9岁”[322]。而自中国政府于6月6日召开新闻发表会后，六四事件的实际死亡人数和天安门广场伤亡问题便不断出现争论，有“死者都在天安门广场外”[323]与不同日期的说法。原因在于中国政府展开军事镇压后，便不断控制任何资讯的发布；之后则严格禁止在中国境内研究相关主题，使得今日对于实际死亡和受伤人数仍然不清楚。各方来源提供的伤亡估计，各说各话下亦有很大的出入，声称人数从无人丧生至数千人丧生的说法都有[7]。
//
//根据《中华人民共和国国务院公报》（1989年第11号）所提供资料，中国人民解放军军队、武警及警察有数十人被害，6000多人负伤；非军人有3000多人受伤，200余人死亡，包括36名大学生、医护人员、群众[324]。海外报道的死亡人数一般显著多于大陆报道。2014年美国白宫解密文件显示约有10454人死亡、40000人受伤（白宫的报告引述自戒严部队的消息人士提供的中南海内部文件）。2017年底，英国国家档案馆解密的文件显示，有中国国务院的成员（名字在档案中被涂黑）称1989年天安门事件至少造成1万多名平民死亡，这是目前最高的数字。[4]
//
//1989年6月21日，纪思道在《纽约时报》专栏提到因为缺乏实物证据，而很难确认伤亡的实际人数，但也提到“合理数字应该是大约有50名士兵或警察死亡，以及400名至800名平民丧生”[5]。美国驻华大使李洁明则表示美国国务院外交官看见军队向未持有武器的群众开火，而美国驻华大使馆的员工也曾前往现场搜集部队杀害民众的证据[325]，走访北京附近的这些医院后认为约有数百人遭到枪杀[326]。子女因为这次事件丧生的丁子霖、张先玲等人则共同成立天安门母亲运动后，持续尝试调查死者家属，并记录死者相关资讯[6]；天安门母亲运动确认的失踪与死伤者清单从1999年提出的155人、2005年提出的187人、2010年提出的195人[6]，在2011年8月共有202人获得确认[6]。在天安门母亲运动所搜集的死者资料中，除了因事件而直接丧生的民众外，还包括4名自杀人士、以及6月4日后因抗议活动而遭杀害者，是目前有名有姓者[6][327]。
//
//另一方面，根据吴仁华对于戒严执法行动的相关研究，除去治安人员只有15名军人经确认是因为遭到杀害而丧生[234]。在已经确认死亡原因的15名军事人员中，有6名士兵是因为搭乘的卡车翻覆，车体随后燃烧而丧生[108]；一名隶属第39集团军宣传单位的摄影师因为没有穿着制服，在拍照过程中遭到枪杀；以及在同年7月4日，一名第24集团军排长因为心脏麻痹逝世[234]。剩下7名军事人员的死亡原因，吴仁华认为应该是排除天安门广场上的示威群众时，在任务过程中阵亡[234]。
//
//此外，中国各地仍有示威者伤亡，由于当地记者较少，没有特意记录而难以估计。但根据国际特赦组织的调查，在6月5日时成都市便至少有300人丧生，其中成都市当地部队使用震撼手榴弹、警棍、刺刀和电击棒攻击平民，而当天晚上据称警方也刻意要求医院不能接受学生或者是提供救护车服务，间接导致死者增加[328]。位于成都的天府广场因此也被称为成都“天安门”[329]。
