//2006年时由中国导演娄烨执导拍摄的《颐和园》同样也提及六四事件，并且有数个影像便是在实际的北京街道上拍摄[462]。然而该片在中国大陆被以电影尚未经过审核而禁止公开上映，但也有一部分原因被认为是因为电影内容涉及到六四事件[463]。2014年时，在由马丁·史柯西斯监制、刘伟强执导的黑帮电影《青龙复仇》中，也有情节描述黑帮成员们一同观看和讨论六四事件的新闻。另外在艺术作品部分，中国艺术家岳敏君以六四事件为灵感而创作的油画作品《处决》在2007年时成为拍卖价格最为昂贵的中国当代艺术作品[464][465]。而同样是中国艺术家的王兴伟则将原本受伤学生躺在单车上抬离现场的新闻照片，以企鹅图像加以取代并且重新绘制出油画作品《新北京》[466]。中国独立音乐人李志创作歌曲《广场》，但该歌曲被各大平台封禁[467]。2019年4月，德国徕卡相机公司上线了一支名为“徕卡-狩猎（Leica-The Hunt）”的系列广告片，因片中涉及中国的部分提及六四事件，此片被中国封杀[468]。
