//木樨地冲突
//6月3日晚间，国营电视台陆陆续续警告北京市居民留在室内[222][223]；但受到前两周成功阻挡军队的激励，大批市民仍然走上街头以阻止部队行进[224]。中国人民解放军部队从北京市各个方位逐步向天安门广场推进，分别由第38集团军、第63集团军和第28集团军负责西面；空降兵第15军、第20集团军、第26集团军和第54集团军负责南面；第39集团军和卫戍第1师负责东面；以及第40集团军和第64集团军负责北面[207]。大约晚上10时，第38集团军在广场西方约10公里的长安街五棵松十字路口，开始向示威群众开枪[6][207]。群众对于军队下令实弹射击感到惊讶外，转而开始向部队丢掷物品[207]。当天晚上，32岁的航天技术人员宋晓明成为首位经证实的死者[207]。之后军队遭指控使用射入人体会碎裂，进而造成严重创伤的达姆弹[161][225]。 10时30分，由于民众将双节无轨电车推到路上并放火焚烧，行进中的军队被迫暂时停在天安门广场西侧约5公里的木樨地，试图清除这些临时路障[226][227]。住在附近公寓的居民亦出面试图拦阻军方车队，但第38集团军再度开火，并造成重大人员伤亡[161][108][221]。根据天安门母亲运动调查后提出的死者报告中，共有36人在木樨地死亡[221][6][226]。另外士兵还向木樨地附近的公寓开火，造成在建筑阳台或室内有人因而遭到枪杀[194][221]，这包括数名在公寓观察事态发展的中国共产党高级党政官员[221]。第38集团军最后以装甲运兵车将电车车厢撞开，并持续与尝试仓促搭建路障或组织人链的示威群众对峙[221][228]，之后部队行经长安街经过南礼士路、复兴门、西单到天安门期间都有造成伤亡[207][229]。而负责南面的空降兵第15军伞兵也使用实弹进行射击，并且在珠市口、天桥和前门等地也造成平民伤亡[6]。但是亦有说法认为该类说法均为远距离观察导致的失真，在现场的CBS记者理查德·罗斯也称并未有士兵开枪，而仅仅试图驱散人群，这使得究竟是否发生开枪事故存疑。[230]
