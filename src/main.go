//中国的形象在六四事件发生后，从原先展开现代化改革的反苏盟友，转变成为镇压示威群众的类斯大林威权主义国家。之后反对中美间自由贸易的蓝队阵营也多次提及六四事件，并且以此认为中国对于维护世界和平和美国自身利益都将构成威胁。军队以武力实施清场的消息也对中国经济产生显著的影响，包括世界银行、亚洲开发银行和部分外国政府都要求暂停给中国提供贷款[376]；而除了许多外国直接投资的承诺遭到取消外，中国的旅游观光业收入也从原先的22亿美元减少至18亿美元[377]。中华人民共和国务院总理李鹏在1992年1月31日访问联合国安全理事会时，就此表示对华经济制裁和武器禁运政策严重妨碍国家主权[378]。
