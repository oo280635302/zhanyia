//晚上8时30分时，由于军队的直升机出现在天安门广场上空使得示威学生在各个大学校园呼吁学生加入其行列。晚上10时，示威学生依照预定时间在民主女神的基座附近举办天安门民主大学的成立仪式[237]。但是10时16分时，由政府控制的扩音器警告说部队可以在实施戒严期间采取任何强制执行的措施[237]。晚上10时30分，随着看见军队以实弹射击的目击者陆陆续续从北京市区西侧和南侧进入天安门广场，在天安门广场上的示威学生与群众也了解到有关暴力事件的消息[237]。在午夜时分，学生扩音器则宣布一名学生在靠近中国人民革命军事博物馆的西长安街遭到杀害，这使得待在广场上的群众陷入忧郁情绪[237]。学生指挥部副总指挥李录随即要求学生保持团结，并且坚持以非暴力的手段以争取继续占领天安门广场[237]；凌晨0时30分，吾尔开希则指控一名北京师范大学女学生在晚间离开校园后遭到杀害，之后吾尔开希便因为突发昏厥而被救护车带离广场[237]。而在这时候，仍然有70,000人至80,000人继续留在天安门广场上[237]。
//
//大约凌晨12时15分，军方开始发射照明弹以提供夜间照明[238]，并且第一辆63式装甲运兵车自天安门广场西侧出现并且从广场前的道路快速通过[237]，大约五分钟后出现第二辆装甲车，两者均往东长安街驶去[210]。凌晨12时30分左右则有2辆装甲运兵车抵达天安门广南侧，学生便陆陆续续向军方车辆丢掷水泥块[237][239]。之后其中一辆装甲运兵车突然发生故障而无法移动，示威群众便用棍棒破坏车辆并且以浇上汽油的棉被放火焚烧。而因为车辆遭到焚烧而紧急逃出的3名军人则被示威群众殴打，但学生则组织警戒线并且护送3人到位于广场东侧中国国家博物馆的急救站医治[237]。之后在承受极大压力的情况下学生领袖曾经一度放弃非暴力手段并且准备展开报复行动[240]，其中柴玲便曾一度使用扩音器呼吁学生准备对抗“无耻的政府”[240]。但最后她和李录同意继续维持和平手段的做法，并且没收学生所持有的棍棒、石块和玻璃瓶等可能被视为武器的物品[240]。
//
//凌晨约1时30分，第38集团军和空降兵第15军的队伍前沿分别到达天安门广场的南北两侧[241]。他们开始封锁天安门广场四周并且将里面的示威学生和前往支援的居民分隔开离，而在过程中也杀死数名示威群众[39]。同时第27集团军与第65集团军从天安门广场西侧的人民大会堂出现，而第24集团军也于东侧的中国国家博物馆开始进行部署[240][242]。在遭到军队包围之后，数千名仍然留在广场上的示威学生与群众则开始往广场中央的人民英雄纪念碑聚集[243]。凌晨2时后，部队开始尝试对人民英雄纪念碑旁的示威群众施加压力[244]；而学生广播则不断呼吁军队放弃使用武力，并且提到：“我们是和平请愿，是为了祖国的民主自由，为了中华民族的富强，请你们顺从人民的意愿，不要对和平请愿的学生采取武力……[241]”大约凌晨2时30分，几名工人则开始在人民英雄纪念碑架设其从装甲运兵车上拆解的机枪，并且发誓要向杀害许多示威群众的部队报仇[245]。之后在侯德健劝说下工人们选择放弃武器[245]，而刘晓波则在纪念碑的栏杆公开砸坏另外一枝没有子弹的步枪以重申非暴力运动的立场[245]。
