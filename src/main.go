//群众撤离
//之后曾在木樨地看见军队枪杀民众的北京高校学生自治联合会常委邵江呼吁由知识分子带领示威学生与群众撤离广场，并且表示已经有太多人丧生。刘晓波原本表示不愿意撤离广场，但最后仍被说服并且和周舵、高新和侯德健一同与学生领袖商讨撤离问题[241]，不过包括柴玲、李录和封从德等人在听到撤离意见后最初都拒绝撤离[241]。凌晨3时30分，在隶属于中国红十字会的两名医生建议下，侯德健和周舵同意先行尝试与士兵进行谈判[245][246]。他们随即乘坐救护车抵达天安门广场东北角并且与第38集团军336军团政治委员季新国会面[243]，季新国随即向戒严部队指挥总部转达请求并且获得同意为学生开辟往东南方安全撤离的通路[245][243]。凌晨4点时天安门广场上的灯光突然熄灭，同时官方的扩音器宣布：“现在开始清场，同意同学们撤离广场的呼吁。[241][247]”不过此时学生们则是一同唱起《国际歌》[248]，并且认为军队准备执行最后一项清场任务[245]。
//
//凌晨4点30分时，天安门广场重新开启照明并发射一连串的红色信号弹[210]。与此同时，部队开始从四面八方逼近纪念碑[135][249]，随后部队在聚集于人民英雄纪念碑的示威群众周围10米处重新部署[249]。而侯德健回来后先是尝试说服事先知情的学生领袖接受他与部队的协议，在大约4时32分侯德健透过学生的广播表示他先行和部队达成谈判[249]，然而许多第一次知道这次会谈的学生则气愤地指责他过于胆怯[249]。封从德之后则在广播中解释由于已经没有时间召开紧急会议，而将以口头表决的方式决定示威学生之后的集体行动。但尽管“坚守”的声音比起“撤离”还要来得更加响亮，封从德仍然表示“撤离”意见较为多数而决定带领群众撤离天安门广场[226]。不过大约在4时40分时，穿着迷彩服的士兵冲向人民英雄纪念碑并且破坏学生的广播设施[249][250]；而其他部队则殴打数十名在纪念碑旁的学生，并且扣押或者破坏他们的相机和录音设备[249]。随后士兵开始强制驱散在人民英雄纪念碑附近的群众，之后也有学生和教授尝试说服仍然坚持坐在纪念碑底层的学生离开[249]。
//
//大约早晨5时10分学生们开始离开纪念碑，示威群众们手牵着手往广场东南角安排的通道撤离[135][226][251]，不过由于当时坐在广场北部的学生颇多，因此有相当一部分学生是从广场北侧离去[249][210]。而这时军方则要求那些拒绝离开天安门广场的学生必须加入撤离行列[250]，除了以对空开枪的方式要求剩下的群众撤出广场外[249][252]，并且还调动59式战车部队封锁前往广场的道路[253][254]。而据从北侧撤离的学生所说，军队在纪念碑北面架起机枪向学生扫射[210]。在确认所有示威群众都离开广场后，军方派遣军用直升机运送大型塑料袋并且命令士兵开始清理广场[254]。北京医护人员对香港记者说，军队将广场的尸体装进塑料袋，由军用直升机运走[210]。而6月4日早上6时，已经撤离天安门广场的学生队伍在沿着西长安街自行车车道上准备走回校园时，西单邮电局方向有3辆从天安门广场出发的坦克发射催泪弹并且冲撞人群，造成11名学生受伤[255][256][257][258]。6月4日上午数千名先前撤离的示威群众、清场行动中伤亡者的父母以及被政府举动激怒的工人尝试从东长安街重新回到天安门广场[161]，但是当人群靠近部队时士兵则随即往群众处开枪射击作为警告[161][259]。然而由于有数人因而遭到部队枪击，之后暂时撤离该处的不满群众便又会尝试重新前往占领广场空地[161][260]。之后群众多次尝试进入天安门广场内，但是军队则持续负责管理广场并且持续两周都不向大众开放[261]。
