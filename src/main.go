//事件后续
//后续示威
//参见：王维林
//北京市区自6月4日军队已经控制天安门广场后便逐渐恢复稳定状态，不过在6月5日时西方媒体所拍摄和录制的王维林于长安街阻挡坦克行进的影像轰动世界各地，照片被视为整个六四事件标志性的照片之一[262]。之后香港和澳门随即发起大规模示威游行以声援北京的示威群众，其中澳门有十多万至二十万人参与游行抗议，占当时澳门人口的一半，该次游行亦是澳门历史上规模最大的游行[263]，另外也有一些国家也对于军队清场一事发起抗议活动。曾经加入天安门广场抗议活动的学生返回原本校园以及部队实施清场的消息传开后，中国内地包括成都市、西安市、武汉市、南京市、上海市和广州市等城市都在都爆发大规模的抗议行动并且持续数天。在广州，数万学生曾将主要干道海珠桥占领了四天，整个城市交通陷入瘫痪。到8日，因军队即将进城，人潮只得散去。[264] 而根据国际特赦组织的调查，在6月5日时成都市便至少有300人丧生。其中成都市当地部队使用震撼手榴弹、警棍、刺刀和电击棒攻击平民，而当天晚上警方也刻意要求医院不能接受学生或者是提供救护车服务[265]。而西安市在6月5日到6月6日期间，当地学生也发起大规模游行活动，并且联合参与罢工的工人开始设置路障[266][267]。不过6月8日时陕西省人民政府便表示城市已经稳定下来，并且提出“先稳住动乱分子，尽量避免发生正面冲突、激化矛盾”的执行方针[268]。
//
//上海则是在6月5日时由学生发起示威游行并且开始摆设路障，工厂工人亦发起大规模的罢工抗议行动[269]。这些举动使得铁路与道路交通陷入瘫痪，同时早上许多大众交通工具也无法正常提供服务[269]，英国广播公司则报导说：“数万名工作人士因而无法正常上下班。[270]”第二天在中共上海市委要求下，上海市人民政府派遣6,500人清除道路上的路障以恢复通行[271]。然而当天晚上8时30分，来自北京的161次列车撞死在光新路道口5名尝试要封锁火车来往的群众，晚上10时时现场便聚集超过30万人并导致造成交通中断，而群众也开始殴打火车司机以及工作人员并且焚毁数辆火车车厢以表达不满[271][272]:1199。6月7日时，同济大学、华东师范大学和上海理工大学等学校学生纷纷强占各自学校的礼堂以及教学大楼，并且将其安排成灵堂以悼念六四事件的伤亡者[273]。越来越多学生们参与架设路障阻碍交通的行动，但在听闻上海也有可能戒严后便有3,000多人决定暂时离开校园。当天晚上，中共上海市委副书记兼市长朱镕基发表电视谈话，并且提到：“作为市长，在此郑重声明，市委、市政府从来没有考虑过要使用军队，从来没有打算实行军管或戒严。我们只追求稳定上海、稳定大局，坚持生产、保障生活。[268]”
//
//6月5日时，武汉当地约有20,000名大学学生决定游行到天安门广场表达不满[274]，另外示威群众也分别封锁武汉长江大桥的交通以及集结在武汉站前的广场[274]。第二天学生继续在街头游行示威并使当地交通被迫中断，同时约有10,000名学生决定直接在铁轨上静坐抗议，这导致来往北京、武汉到广州的铁路路线被迫中断[275]，此外学生还鼓励当地各大大企业的工人发起罢工活动[275]。6月7日凌晨学生们开始以公车和路障阻碍交通，并纷纷于各处架设灵堂来纪念六四事件的伤亡者[276]。然而一小群激进的学生拦下一辆货运列车并且开始在列车上泼洒汽油，不过在准备焚烧时成功被警方阻止[276]。但这使得当地警方与居民关系渐趋紧张，居民开始提领现金并且抢购物资[276]。而6月5日、6月6日到6月7日，南京当地学生发起游行活动并四处发表演讲，此外示威学生亦封锁附近联络交通并尝试联合工人发起罢工。6月7日早上7时左右，包括河海大学在内几间大学400多名学生以4辆公共汽车驾封锁南京长江大桥持续到傍晚[277]，而南京大学为主的在校学生则在中央门各处摆放路障[277]。一直到当天下午4时以后学生与群众才被说服并且撤离[277]，也让交通一度恢复[277]。然而6月8日，包括南京大学与河海大学等校学生重新控制了南京站周遭一公里的交通[278]，同时学生们也陆陆续续于南京长江大桥上静坐表达不满[278]。对于学生激烈的反应，中共江苏省委认为当地局势已渐趋失控，并陆续向学生表示公安部将会严惩行动的策划人[278]。
