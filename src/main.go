//九二南巡
//参见：邓小平南巡
//此时受到示威抗议的影响使得中国政府在改革政策制定上采取保守态度，并且认为示威抗议的原因便是由于改革速度过快的结果[385]。而主持改革开放政策的邓小平在示威活动爆发后其影响力则显著降低，这迫使他必须与坚持社会主义的强硬派做出妥协[393]。但之后同样支持邓小平改革开放政策的赵紫阳被迫远离政治核心，同时在保守阵营的压力下邓小平也与国家政策决定开始疏远[385]。在以江泽民为首的领导集体确立后[123]，邓小平便在稍后以江泽民为“合格的党总书记”为由让其接任中国共产党中央军事委员会主席，自己则退出了中国共产党的领导高层[394]。然而政府改革开放脚步过慢一方面引起地方领导人的强烈批评，另一方面受到1990年代苏联解体使得邓小平影响力在保守派势力中逐渐恢复。
//
//1992年，邓小平在中国南方的深圳、珠海、广州、上海等地所做的巡视以及讲话，重申与改革开放相关的邓小平理论。其中，邓小平批评在示威抗议结束后掌权的社会主义强硬派，并且称赞创业精神和其他以市场为导向的政策。最初该意见遭到中国共产党相关媒体的忽视，但是最后中国共产党中央政治局则发布正式命令确立邓小平的经济改革开放作法[385][395]。邓小平的九二南巡在中华人民共和国历史上具有重要地位，因六四事件而放缓甚至停滞的中国大陆改革开放自此重新提速[396][397][398][399]。
//
//审查制度
//参见：中华人民共和国出版物审查制度、中华人民共和国电视审查和中华人民共和国网络审查
//中国共产党严格限制有关六四事件的讨论，并且已经采取措施阻止或者审查相关资讯信息[400]。其中大陆教科书几乎没有任何相关的讯息[401][402]；即使不得不有所提及的时候，相关内容也多是以中国共产党官方观点编写而成[403]。在抗议活动结束后，政府官员查禁许多有争议的电影和书籍并且关闭多家报刊出版社。在往后1年内，中国国内近12%的报纸、13%的社会科学期刊以及150部电影遭到取缔，还有将近8%的出版公司也被迫关闭歇业，此外政府还宣布他们已经查获3,200万本违规的书籍刊物和240万片违禁的影像记录[404]。之后有关六四事件的新闻媒体内容和互联网资源都因为实施审查制度而遭到限制或者是封锁，而其中受到金盾工程的影响使得包含六四事件等互联网内容在中国境内遭到封锁而无法浏览[405]。
//
//其中在中国大陆有许多文学作品因为与六四事件相关而遭到禁止出版，例如收集许多六四事件期间抗议诗集的《六四诗集》便遭到禁止，而在2006年时小说《紫禁城》也因为其相关内容而遭到查禁[406]。此外有关采访赵紫阳或者是其秘书鲍彤的书籍也遭到禁止[407][408]，其中包括由杨继绳于秘密采访赵紫阳并且整理其他否决中国政府决定之资料所撰写而成的《中国改革年代的政治斗争》[409]，以及受到政府压力而无法出版的宗凤鸣著作《赵紫阳软禁中的谈话》等书[410]。比较特别的是在2009年5月时，赵紫阳秘密录下的录音纪录成功偷运至海外，并且另外编写出版了《改革历程》一书[411]；而在2010年时，另一本依据李鹏在六四事件期间的日记整理后也以《李鹏六四日记》为名出版[412]。
//
//在中国的互联网上搜寻“六四”或者是“天安门”等相关词语，则只会提供经过审查后的结果，甚至在有的时候则会短暂切断与服务器的连接[413][403]。其中有一部分是借由关键字审查来将网站的特定页面加以封锁，但是对于像海外中华人民共和国民主运动等网站则会直接遭到中国政府完全禁止进入[413]。但是中华人民共和国网络审查制度却往往并不一致，包括YouTube、维基百科和Flickr等许多网站长期以来便多次遭到封锁然后又获得解禁；此外审查制度对于中文网站来说也比外语网站还要来得严格许多[414]。在2006年1月时，Google同意在其中国大陆页面审查有关六四事件和其他中国政府认为“敏感”之内容[415]，不过2010年1月时宣布撤除相关的审查合作[416]，而百度百科在条目1989年的6月章节中的6月4日仅提及波兰团结工会候选人瓦文萨当选总理，以及伊朗总统哈梅内伊获推选为伊朗最高领袖，而对于六四事件只字不提。
//
//而后十余年间，整个运动的压制开始略有松绑以来，仍不少教科书谈及六四事件时大多以“清场”而非“镇压”来形容事件，并以“平息事件”来结尾。而于香港，当地的教育局公布的课程修订大纲中没有提到“六四事件”，而被问及时表示因为“未有纳入的事件因在中国历史发展中并不重要”（然而重大的九二南巡与此事件直接相关）[417]。媒体纪录片方面，则在2021年6月20日由央视放映了《敢教日月换新天 第十一集 举旗定向》，片中以1989年之后中国开始加速体制问责，此后社会相对独联体国家急速发展的口吻，并客观上逐渐得到了高度肯定的政治评价做结，这次公开播出是官方媒体沉默32年后，首向大众对此事进行表态[418]。
//
//政治冷漠
//
//一名于天安门广场戒备着的武警上等兵，其后便是天安门城楼上所挂着的毛泽东肖像
//到了今日，由于许多潜在因素使得很多中国公民都不愿意谈论抗议活动的进行[419]。较为年长的​知识分子则不再渴望政治变革并且转而专注在经济改革方面[420]。而许多中国青年都不知道有关抗议活动的经过、王维林等著名象征乃至于6月4日当天的事件等[421]。但尽管如此，每年6月4日当天中国政府也不允许有关于六四事件的活动存在[338][422][423][424]，例如在六四事件二十周年的2009年6月4日当天中国共产党相关机关便在天安门广场附近加强警备，其中除了穿着制服的公安机关和中国人民武装警察部队成员外还有数百名便衣工作人员出现在广场上[425]。之后新闻媒体工作人员被拒绝进入广场内[425]，那些试图在广场上拍摄或者采访持不同政见者的记者则遭到短暂拘留[426][427]。而在六四事件二十五周年前夕，包括高瑜[428]、浦志强、徐友渔、郝建、刘荻、胡石根等人则因为参与相关纪念活动而遭到逮捕[429][430][431]。另外每年六四事件周年纪念期间，中国政府亦会关闭全球社群网络服务的网站链接并且加强对于中国持不同政见者的监视[432][405][433]。
//
//但在这般情况下，包括丁子霖的天安门母亲运动等人士或者组织仍然会继续报导有关六四事件的经过[413]，而多年来一些中国民众也呼吁中国政府重新评估抗议活动并且补偿遇难者家属[434]，如天安门母亲运动便要求赔偿、平反受害者并且归还在中国与海外所筹到的募款金额[341][435]。而曾经参与军事镇压的前军人张世军更是向中央军委主席胡锦涛发表了公开信，期望政府能够就六四事件重新评估其历史价值，但随后他便在自己的家中遭到逮捕[436]。不过虽然中国政府从未正式承认在六四事件期间有从事不法行为，但在2006年4月时则在政府首次公开的情况下接受六四事件相关受害人家属的申诉，并且以“困难补助”的名义向在四川死亡周国聪的母亲唐德英赔偿将近70,000元人民币，其中周国聪在1989年6月6日时遭到警方拘押在成都市并且在2天后逝世[437]。这一举动获得中国许多活动家的欢迎，但是一些人则将这案例视为为了维护社会稳定的举动，并不认为是预告中国共产党官方立场将有所变化的行动[438][439]。
//
//直到八九民运三十周年，即2019年时，六四事件的话题在中国仍然是敏感话题。法国广播电台报道认为，一批香港记者在事件三十周年之际，以摄制纪录位片、撰写文章和口述当年事件的所见所闻的方法，以传承历史真相；另一方面，法广认为一些亲建制派传媒由过往不主动提及事件以达到让人遗忘的目的，转为以讽刺、批评及错误论述等方法营造“六四事件可以争拗”的现象[440]。中共中央机关报在运动中期称其为“动乱”，后谴责为“反革命暴乱”，至今六四事件在中国大陆仍为敏感话题被严格限制讨论或悼念[441]。尽管事件本身对共产党的影响力不断减弱，在中国大陆以外地区仍有许多人主张“转型正义”[41][29]。
//
//观点变化
//在此事件结束后多年，特别是进入2000年后，中国经济的高速发展使得民众生活水平普遍得到提高，并使中国成为继美国之后的全球第二大经济体及军事强国。国家实力的提升，使得对中国大陆民众因经济差距倒向“西方怀抱”的担忧被消解，中国政府和社会主义制度的合法性得以稳固。[442]2010年皮尤研究中心的调查认为，中国政府的受信任度和合法性仍然很高，其从2007年的83%增加到2010年的87%[443]。研究还发现，有87%的中国人满意他们的政府，74%的中国人认为他们的政府正走在一条正确的道路上。另外，诸多国家（包括苏东剧变中涉及的国家和利比亚、2014年后的乌克兰等）发生西方国家支持的“颜色革命”并造成政权更迭后，长期处于经济低迷甚至陷入政局动荡，使得出于中国时局动荡、国家分裂这一假设的担忧日益增加。
//
//在此背景下，中国社会对六四事件中政府措施持否定态度的舆论观点有所减少。到2010年代，支持政府的立场更不断升温。政府结束运动的方式获得认可，对六四事件本身的负面评价则同时增加，如称之为“胡闹”。而在这部分对事件本身表示否定立场的人群中，甚至有当年事件中的民主派人士[444][445][446][447]。而中国大陆的部分公众人物（如互联网中的网络红人马云）还开始在言论中引用六四清场这一事件，其立场则直接体现出对政府的支持[448]。六四清场中，政府使用坦克清场的方式更是被大量中国大陆民众认为是暴力镇压最为有效的手段。例如，2019年6月香港反对逃犯条例修订草案运动爆发，中国大陆舆论与香港严重对立。而在中国大陆民众呼吁中央政府直接以武力镇压结束的呼声中，即有人提议使用六四清场方式，出动坦克驱离，直接结束运动[449]。
//
//文化作品
//在学生示威活动遭到镇压后，于1989年6月4日当天正在罗马举办演唱会的英国摇滚乐团治疗乐队，在最后安可曲中将“信仰”（Faith）一词改为“今日于中国死去的每个人”（everyone that died today in China）[450]。比利·乔在其1989年9月发布的单曲专辑《火不是我们放的》中，则以“中国戒严着”（China's under martial law）一词来将六四事件作为倒数第二个引用的历史事件[451]。而前卫摇滚乐团海狮合唱团则在创作的歌曲《日落城之王》中使用天安门广场来作为印象图片，同时歌曲内也出现“六四的傀儡皇帝”（a puppet king on the Fourth of June）等歌词[452]。同年11月时琼·拜亚在她的专辑《Speaking of Dreams》中亦以歌曲《中国》（China）来纪念六四事件，之后她亦说到专辑是为了“坚持非暴力手段而付出巨大代价、已经永远改变中国面貌”的天安门广场上之学生[453]。而在华语音乐中，由黄霑创作、张学友演唱的歌曲《人间道》以“大地江山，怎么会变成血海滔滔，世间怎么尽是无间道”等歌词对六四事件进行了影射[454]。R.E.M.则是在1991年推出了《闪亮快乐的人》，在专辑歌曲上讽刺性的安排有关中国政府对于六四事件的宣传内容翻译[455]。1992年时，罗杰·沃特斯也推出了专辑《欢愉致死》，其中在歌曲《看电视》（Watching TV）中对于西方世界就六四事件的反应进行沉思[456]。到了2013年，AKB48推出了专辑《Team A 6th Stage“目击者”》，其中在歌曲《目击者》中就有疑似影射六四事件的相关内容，也因此导致在2014年SNH48引进同名专辑时该歌被删除[457]。
//
//1989年，汤姆·布洛考在自己主持的黄金时段上将天安门广场的示威活动与柏林墙倒塌同样列为当年度世界上最重要的人权事件之一[458]。2009年6月3日，英国广播公司则播出纪录片《重返天安门》（Kate Adie Returns to Tiananmen Square），由记者凯特·阿迪再度前往中国并且回忆在1989年所目睹的事件发生经过[459]。另外由电视广播有限公司在2009年制作的电视剧《天与地》，其编审周旭明表示剧本在创作时受到六四事件的启发，而这部电视剧后来则在2011年时遭到国家广播电影电视总局下令禁止播出。而在1992年时，由李国豪主演的电影《龙霸天下》则于电影中出现六四事件的部分场景，在电影中李国豪扮演一个在六四事件期间死亡的美国政府雇员其儿子[460][461]。《辛普森一家》“Goo Goo Gai Pan”一集中，当辛普森一家穿过天安门广场时，广场上的一块石碑上写着“1989年，天安门广场，这里什么也没有发生”。《南方公园》“电影预告片”一集中，片中角色称1993年在天安门广场上有很多人死亡，事实上是指1989年的六四清场。
