//而中国共产党上海市委员会书记江泽民之后取代赵紫阳成为中国共产党中央委员会总书记[388]，其中江泽民在上海市处理具有改革派倾向刊物和积极响应四二六社论的作法使得他赢得中国共产党党内保守势力的支持[135][288]。这次抗议活动也让原本依照1982年宪法与往常惯例采取的权力分立有所修正，过去为了防止毛泽东的个人统治以及相关的过激行为，中国共产党决定废除毛泽东终身担任的中国共产党中央委员会主席职务。同时重新设立自1975年被废除的中华人民共和国主席职务，并将其列为没有实权的虚位元首职务，同时实际上真正拥有政治权力的中共中央总书记、国务院总理以及中央军委主席则由不同人担任。然而在六四事件期间，担任国家主席的中共元老杨尚昆同时兼任中央军委副主席和秘书长掌握军权，并且公然与另一位中央军委副主席、中国共产党中央委员会总书记赵紫阳决裂，并且转向主张使用军事武力的国务院总理李鹏和中央军委主席邓小平一派，这使得官方政策变得不一致和不连贯并且明显阻碍行使权力。为了对外交流的身份需要，自1993年起，中共中央总书记、中央军事委员会主席兼任国家主席的职务[389][390][391]。
//
//邓小平离开领导层后，随着“废除干部领导职务终身制”实行，陈云、杨尚昆、薄一波、习仲勋等中共元老亦逐渐淡出政坛。之后江泽民、李鹏、朱镕基、李瑞环等人组成的新一代中央领导集体正式接班。之后，中华人民共和国政府声称“为了维护社会稳定，对政治风波采取了果断措施”[392]，进行了大规模逮捕示威民众和支持者，并镇压其他地区进行的抗议活动；同时严格控制国内新闻媒体对事件之报导，并禁止外国记者将相关照片和采访影像发送至国外。废除干部领导职务终身制在后邓小平时代一直延续下来，直至2018年修宪后才被废除。
