//加强控制
//
//6月9日，在80年代担任最高领导人的邓小平发表公开谈话，并批评示威活动者的目的
//军队在6月4日镇压天安门广场的示威群众后，中国政府开始加强控制新闻媒体和公民言论等自由，同时因六四事件惩处国内外媒体工作者[279]。尽管国家媒体在军队实施清场刚发生后大多报导同情学生的内容，但是之后所有负责人遭到撤离职位作为处分，或者接受检讨和人人过关。其中在6月4日至6月5日在中国中央电视台播出的《新闻联播》上，个别负责报导此次事件因表现悲痛情绪的4名新闻主播分别是杜宪与张宏民、薛飞和李瑞英搭档。但事件之后央视过关学习，杜和薛的态度不改（却被调离处分三年后先后离职），而李张等人检查符合要求，才有了日后的天壤之别 。中国国际广播电台英语部节目副主任、同时也是前中华人民共和国外交部部长吴学谦儿子的吴晓镛和英语播音员陈原能也因为其负责的节目对示威群众表示同情之意[280]，后来吴事后被捕，陈则被禁止出国。而《人民日报》由于发表同情示威群众的报导，包括社长钱李仁与总编辑谭文瑞等编辑都因而撤职[238]。而包括吴学灿等编辑人员则是因为在未经许可的情况下出版特别相关报导，纷纷被判处有期徒刑4年[281]。
//
//所有国际新闻媒体在北京市实施军事行动期间被勒令停止广播，同时中共当局早在5月24日就关闭了卫星传播的线路[282]。然而许多广播公司仍然无视这些禁令而尝试借由试电话向外界报告情况，而许多相关的拍摄影片纪录也很快被偷偷地运出中国大陆，这也包括西班牙电视台在6月4日凌晨所拍摄的天安门广场情势独家纪录[283]。而在军事行动期间一些外国记者则遭到中国大陆有关当局的骚扰，其中哥伦比亚广播公司的记者理查德·罗斯（Richard Roth）以及搭档摄影师便遭到中国大陆当局拘留，然而在他仍然不断透过移动电话报导天安门广场的情况[284]。之后几名因为报导中国大陆当局派遣部队清场的外国记者在随后几个礼拜遭到驱逐出境，而其他记者则持续遭到中共当局骚扰或者是被列入黑名单之中[285][286]。各国驻上海领事馆则被中国大陆当局告知并无法保证记者的人身安全，并开始要求每家新闻媒体必须遵从新颁布的准则内容[287]。
//
//6月9日，中国共产党中央军事委员会主席邓小平在其他党政高层的陪同下前往中南海怀仁堂接见戒严部队高级干部，而这也是自从学生发起示威活动以来邓小平首次于公开场合出现[135][288][135]。邓小平在之后演讲中称呼因为六四事件而丧生的解放军士兵为“烈士”[289][290]，并且指出示威活动的目的是为了推翻共产党以及国家，希望能进一步“建立一个完全西方附庸化的资产阶级共和国”[291]。邓小平认为示威者之所以不断强调包括官员贪污等等有关的投诉，便是为了掩盖其底下试图将当前社会主义制度加以取而代之的真正动机[292]。他之后还以此观点表示：“这整个都是仍坚持帝国主义之西方世界的计划，他们企图让所有社会主义国家逐一放弃社会主义道路，然后将它们带往另一条充斥着国际资本以及垄断资本主义的道路。[293]”
